'''
Programa Orientada a Objetos - POO
 - POO é um paradigma de programação que utiliza mapeamento de objetos
 do mundo real para modelos computacionais.

 - Paradigma de programação é a forma/metodologia utilizada para pensar/desenvolver sistemas

 Orientaçã a objetos:
 - Classes : Modelo do objeto do mundo real sendo representado computacionalmente
 - Atributo: Característica do objeto
 - Método: Comportamento do objeto (funcao)
 - Construtor: Método especial utilizado para criar os objetos
 Objeto: Instância da classe
'''
numero = 10
print(numero)
print(type(numero))

stg = "Geek"
print(stg)
print(type(stg))

class Produto:
    pass

ps4 = Produto()
print(ps4)
print(type(ps4))